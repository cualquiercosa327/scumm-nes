const convertPalette = (nesPalette) => {
  const palette = [];
  for (let i = 0; i < nesPalette.length; i += 3) {
    palette.push(
      `#` +
        hex(
          (nesPalette[i] << 16) |
            (nesPalette[i + 1] << 8) |
            (nesPalette[i + 2] << 0)
        ).padEnd(6, '0')
    );
  }
  return palette;
};

